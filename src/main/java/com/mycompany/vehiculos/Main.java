    private void listarFuncionariosiud() {
        
        cbxFuncionarios.removeAllItems();
        Funcionariosiud funcionariosiud1 = new Funcionariosiud();
        funcionariosiud1.setNombres(SELECCIONE);
        funcionariosiud1.setApellidos("");
        cbxFuncionarios.addItem(funcionariosiud1);
        DefaultTableModel defaultTableModel = new DefaultTableModel();
        for (String COLUMN : COLUMNS) {
            defaultTableModel.addColumn(COLUMN);
        }
        tblFuncionariosiud2.setModel(defaultTableModel);
        try {
            List<Funcionariosiud> funcionariosiud = funcionariosiudController.obtenerFuncionariosiud();
            if (funcionariosiud.isEmpty()){
                return;
            }
            defaultTableModel.setRowCount((funcionariosiud.size()));
            int row = 0;
            for (Funcionariosiud funcionarios : funcionariosiud){
                defaultTableModel.setValueAt(funcionarios.getCedulaf(), row, 0);
                defaultTableModel.setValueAt(funcionarios.getNombres(), row, 1);
                defaultTableModel.setValueAt(funcionarios.getApellidos(), row, 2);
                defaultTableModel.setValueAt(funcionarios.getEstadocivil(), row, 3);
                defaultTableModel.setValueAt(funcionarios.getSexo(), row, 4);
                defaultTableModel.setValueAt(funcionarios.getDireccion(), row, 5);
                defaultTableModel.setValueAt(funcionarios.getTelefono(), row, 6);
                defaultTableModel.setValueAt(funcionarios.getFnacimiento(), row, 7);
                row++;
                
                cbxFuncionarios.addItem(funcionarios);
            }
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }
    private void addListener(){
        cbxFuncionarios.addItemListener(event -> {
             Funcionariosiud selectedFuncionarios = (Funcionariosiud) event.getItem();
             if (selectedFuncionarios.getNombres().equals(SELECCIONE)) {
                 txtCedulafEdit.setText("");
                 txtNombresEdit.setText("");
                 txtApellidosEdit.setText("");
                 txtEstadocivilEdit.setText("");
                 txtSexoEdit.setText("");
                 txtDireccionEdit.setText("");
                 txtTelefonoEdit.setText("");
                 txtFnacimientoEdit.setText("");
             }else {
                txtCedulafEdit.setText(selectedFuncionarios.getCedulaf());
                txtNombresEdit.setText(selectedFuncionarios.getNombres());
                txtApellidosEdit.setText(selectedFuncionarios.getApellidos());
                txtEstadocivilEdit.setText(selectedFuncionarios.getEstadocivil());
                txtSexoEdit.setText(selectedFuncionarios.getSexo());
                txtDireccionEdit.setText(selectedFuncionarios.getDireccion());
                txtTelefonoEdit.setText(selectedFuncionarios.getTelefono());
                txtFnacimientoEdit.setText(selectedFuncionarios.getFnacimiento());
                
             }
        });
    }
